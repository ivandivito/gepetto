.INCLUDE "gepetto.inc"

.DSEG
CURRENT_STATE: .BYTE 1

.CSEG
.ORG 0x00
	JMP MAIN

.ORG INT0addr
	JMP SOFT_UART_INTERRUPT

	
.ORG INT_VECTORS_SIZE
MAIN:
	
	;INICIALIZACION
	
	;Inicilizacion Sistema (stack pointer, timers, etc)
	
	CLR ZERO_REG
	
	LDI R16, LOW(RAMEND)
	OUT SPL, R16
	LDI R16, HIGH(RAMEND)
	OUT SPH, R16
	
	CALL BUTTONS_TIMER_INIT
	
	;Inicializacion SOFTUART (Micro interprete)
	
	LDI R16, SOFT_UART_DEF_SUBR
	STS SUBR, R16

	CALL SOFT_UART_INIT
	
	;Inicializacion USART (PC)
	
	;Inicializacion SPI (SD)
	
	SEI
	
	;Verificar programa guardado
	
	;Configurar e inicializar GRBL

.DEF STATE_REG = R16
	
MAIN_LOOP:
	;Cargar estado
	LDS STATE_REG, STATE
	
	CPI STATE_REG, STATE_IDLE
	BREQ MAIN_IDLE
	
	CPI STATE_REG, STATE_CONNECTED
	BREQ MAIN_CONNECTED
	
	CPI STATE_REG, STATE_RUNNING
	BREQ MAIN_RUNNING
	
	RJMP MAIN_ERROR
	
	MAIN_IDLE:
		CALL IDLE_RUN
		RJMP MAIN_LOOP
		
	MAIN_CONNECTED:
		CALL CONNECTED_RUN
		RJMP MAIN_LOOP
		
	MAIN_RUNNING:
		CALL RUNNING_RUN
		RJMP MAIN_LOOP
		
	MAIN_ERROR:
		CALL IDLE_ERROR
		RJMP MAIN_LOOP
	
	